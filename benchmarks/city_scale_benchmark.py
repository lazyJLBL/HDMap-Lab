from __future__ import annotations

import argparse
import json
import random
import statistics
import sys
import time
from pathlib import Path
from typing import Any

import psutil

sys.path.append(str(Path(__file__).resolve().parents[1]))

from app.core.bbox import bbox_of_coords
from app.geometry_kernel.polyline import polyline_length
from app.spatial_index import BasicRTreeIndex, BruteForceIndex, GridIndex, QuadTreeIndex, STRRTreeIndex


def main() -> None:
    parser = argparse.ArgumentParser(description="Run a city-scale bbox benchmark on a GeoJSON road extract.")
    parser.add_argument("--roads", default="data/roads.geojson")
    parser.add_argument("--queries", type=int, default=100)
    parser.add_argument("--output", default="docs/city_scale_benchmark_result.md")
    parser.add_argument("--seed", type=int, default=7)
    args = parser.parse_args()

    features = load_roads(args.roads)
    report = run_city_scale_benchmark(features, args.queries, args.seed)
    output = Path(args.output)
    output.parent.mkdir(parents=True, exist_ok=True)
    output.write_text(render_markdown(report), encoding="utf-8")
    print(f"wrote benchmark report to {output}")


def load_roads(path: str) -> list[dict[str, Any]]:
    collection = json.loads(Path(path).read_text(encoding="utf-8"))
    return [feature for feature in collection.get("features", []) if (feature.get("geometry") or {}).get("type") == "LineString"]


def run_city_scale_benchmark(features: list[dict[str, Any]], query_count: int = 100, seed: int = 7) -> dict[str, Any]:
    random.seed(seed)
    roads = [
        {
            "id": str(feature.get("id") or (feature.get("properties") or {}).get("id") or index),
            "coords": [tuple(coord) for coord in feature["geometry"]["coordinates"]],
        }
        for index, feature in enumerate(features)
    ]
    items = [(bbox_of_coords(road["coords"]), road["id"]) for road in roads]
    dataset_bbox = _union_bbox([bbox for bbox, _ in items])
    queries = _sample_queries(dataset_bbox, query_count)
    process = psutil.Process()
    rows = []
    for name, factory in [
        ("brute_force", lambda: BruteForceIndex(items)),
        ("grid", lambda: GridIndex(items)),
        ("quadtree", lambda: QuadTreeIndex(items)),
        ("rtree", lambda: BasicRTreeIndex(items)),
        ("str_rtree", lambda: STRRTreeIndex(items)),
    ]:
        memory_before = process.memory_info().rss
        started = time.perf_counter()
        index = factory()
        build_ms = (time.perf_counter() - started) * 1000.0
        memory_after = process.memory_info().rss
        latencies: list[float] = []
        candidate_counts: list[int] = []
        for query in queries:
            query_started = time.perf_counter()
            result = index.query(query)
            latencies.append((time.perf_counter() - query_started) * 1000.0)
            candidate_counts.append(len(result))
        rows.append(
            {
                "index": name,
                "build_ms": build_ms,
                "memory_delta_kb": max(0, memory_after - memory_before) / 1024.0,
                "mean_candidates": statistics.mean(candidate_counts) if candidate_counts else 0.0,
                "p50_ms": statistics.median(latencies) if latencies else 0.0,
                "p95_ms": _percentile(latencies, 0.95),
                "p99_ms": _percentile(latencies, 0.99),
            }
        )
    return {
        "dataset": {
            "roads": len(roads),
            "bbox": list(dataset_bbox),
            "total_length_m": sum(polyline_length(road["coords"]) for road in roads),
            "query_count": query_count,
        },
        "results": rows,
    }


def render_markdown(report: dict[str, Any]) -> str:
    dataset = report["dataset"]
    lines = [
        "# City-Scale Spatial Index Benchmark Result",
        "",
        "This report is generated by `python -m benchmarks.city_scale_benchmark`.",
        "",
        "## Dataset",
        "",
        f"- Roads: {dataset['roads']}",
        f"- BBox: `{dataset['bbox']}`",
        f"- Total length: {dataset['total_length_m']:.2f} m",
        f"- Query count: {dataset['query_count']}",
        "",
        "## Results",
        "",
        "| Index | Build ms | Memory KB | Mean Candidates | p50 ms | p95 ms | p99 ms |",
        "| --- | ---: | ---: | ---: | ---: | ---: | ---: |",
    ]
    for row in report["results"]:
        lines.append(
            f"| {row['index']} | {row['build_ms']:.3f} | {row['memory_delta_kb']:.1f} | "
            f"{row['mean_candidates']:.1f} | {row['p50_ms']:.3f} | {row['p95_ms']:.3f} | {row['p99_ms']:.3f} |"
        )
    lines.append("")
    return "\n".join(lines)


def _sample_queries(bbox: tuple[float, float, float, float], count: int) -> list[tuple[float, float, float, float]]:
    min_lon, min_lat, max_lon, max_lat = bbox
    width = max(max_lon - min_lon, 1e-9)
    height = max(max_lat - min_lat, 1e-9)
    query_width = width * 0.08
    query_height = height * 0.08
    queries: list[tuple[float, float, float, float]] = []
    for _ in range(count):
        lon = random.uniform(min_lon, max_lon - query_width)
        lat = random.uniform(min_lat, max_lat - query_height)
        queries.append((lon, lat, lon + query_width, lat + query_height))
    return queries


def _union_bbox(boxes: list[tuple[float, float, float, float]]) -> tuple[float, float, float, float]:
    if not boxes:
        return (0.0, 0.0, 0.0, 0.0)
    return (
        min(box[0] for box in boxes),
        min(box[1] for box in boxes),
        max(box[2] for box in boxes),
        max(box[3] for box in boxes),
    )


def _percentile(values: list[float], q: float) -> float:
    if not values:
        return 0.0
    values = sorted(values)
    return values[min(len(values) - 1, round((len(values) - 1) * q))]


if __name__ == "__main__":
    main()
